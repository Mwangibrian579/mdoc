    <h1> Best first Aid remedies before you get to our Hospital;</h1>
    <p>Most are the times when you are faced with medical emeregencies and situations that need first aid and further medical attention in some cases. We have listed some of the first aid measures that can be taken before one receives medical attention. In the event of medical emergency, situation or accident you can apply the measures listed below before medics arrive or before you get to our hospital </p><br>
    <ol>
    <li><h2>Cuts and Wounds</h2></li>
    <img src="./pictures/cutt.jpeg" alt="Cut wound">
    <p>In the case of minor cuts or wounds, start by cleaning the area with mild soap and water. Apply an antiseptic ointment and cover the wound with a sterile bandage. For deeper cuts or wounds with heavy bleeding, apply direct pressure using a clean cloth or bandage, and seek medical attention promptly.</p><br>
<li><h2>Burns</h2></li>
    <img src="./pictures/burns.jpeg" alt="Burn">
    <p>For minor burns, cool the affected area under running water for at least 10 minutes. Avoid using ice, as it can damage the tissue. Cover the burn with a sterile, non-stick dressing. Seek immediate medical attention for severe burns, especially those involving the face, hands, feet, genitals, or major joints.</p><br>
<li><h2>Choking</h2></li>
    <img src="./pictures/chock.jpeg" alt="choking">
    <p>If someone is choking, encourage them to cough forcefully. If the person cannot breathe, perform abdominal thrusts (Heimlich maneuver). Stand behind the person, place your arms around their waist, make a fist, and give quick, upward thrusts. If the person loses consciousness, start CPR and call for emergency assistance.</p><br>
<li><h2>Heart Attack</h2></li>
    <img src="./pictures/heart.jpeg" alt="heart attack">
    <p>If someone shows signs of a heart attack (chest pain, shortness of breath, nausea), call emergency services immediately. Help the person sit down, rest, and encourage them to take aspirin if available. Perform CPR if the person becomes unresponsive.</p><br>
<li><h2>Stroke</h2></li>
    <img src="./pictures/strok.jpeg" alt="Dtroke">
    <p>Recognize stroke symptoms using the FAST acronym (Face drooping, Arm weakness, Speech difficulty, Time to call emergency services). Keep the person calm, and call for help immediately. While waiting for assistance, ensure the person is in a comfortable position and monitor their vital signs.</p><br>
<li><h2>Fractures and Sprains</h2></li>
    <img src="./pictures/fract.jpeg" alt="fractures">
    <p>For suspected fractures, immobilize the affected area with a splint or bandage, avoiding unnecessary movement. Apply ice to reduce swelling. Elevate the injured limb if possible. In the case of sprains, use the RICE method: Rest, Ice, Compression, and Elevation</p><br>
<li><h2>Seizures</h2></li>
    <img src="./pictures/seiz.jpeg" alt="Seizures">
    <p>During a seizure, clear the area around the person to prevent injury. Place the person on their side to help clear the airway. Do not restrain their movements. Time the duration of the seizure. Seek medical attention if the seizure lasts longer than five minutes or if another seizure follows.</p><br>
<li><h2>Allergic Reactions</h2></li>
    <img src="./pictures/allerg.jpeg" alt="Allergies">
    <p>If someone is experiencing an allergic reaction with symptoms like difficulty breathing, swelling, or hives, administer an epinephrine auto-injector if available. Call for emergency assistance and monitor the person's vital signs. If the person loses consciousness, perform CPR.</p><br>
<li><h2>Poisoning</h2></li>
    <img src="./pictures/pois.jpeg" alt="Poisoning">
    <p>If someone has ingested a toxic substance, call poison control immediately. Do not induce vomiting unless directed by professionals. If the person is unconscious or having difficulty breathing, perform CPR and seek emergency medical assistance.</p><br>
</ol>
<p>Incase of an emergency or an accident you can apply the first aid measures above as you <a href="contacts.php">Contact us</a> for medical assistance</p>
